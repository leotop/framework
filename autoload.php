<?php
/** 
 * Autoloader 
 * (is a joke, i know. But its easy to understand!)
 *
 * @author  Pixelbart
 * @version 1.0
 */

//--------------------------------------------------------------------------
// Boot Config
//--------------------------------------------------------------------------

foreach ( glob( "app/Config/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Boot Core
//--------------------------------------------------------------------------

foreach ( glob( "app/Core/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Boot Helpers
//--------------------------------------------------------------------------

foreach ( glob( "app/Helpers/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Start the Session
//--------------------------------------------------------------------------

session_start();

//--------------------------------------------------------------------------
// Boot Models
//--------------------------------------------------------------------------

foreach ( glob( "app/Models/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Boot Controllers
//--------------------------------------------------------------------------

foreach ( glob( "app/Controllers/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Boot Views
//--------------------------------------------------------------------------

foreach ( glob( "app/Views/*.php" ) as $file ) {
    require_once $file;
}
