 */

//--------------------------------------------------------------------------
// Boot Config
//--------------------------------------------------------------------------

foreach ( glob( "Config/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Boot Core
//--------------------------------------------------------------------------

foreach ( glob( "Core/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Boot Helpers
//--------------------------------------------------------------------------

foreach ( glob( "Helpers/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Start the Session
//--------------------------------------------------------------------------

session_start();

//--------------------------------------------------------------------------
// Boot Models
//--------------------------------------------------------------------------

foreach ( glob( "Models/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Boot Controllers
//--------------------------------------------------------------------------

foreach ( glob( "Controllers/*.php" ) as $file ) {
    require_once $file;
}

//--------------------------------------------------------------------------
// Boot Views
//--------------------------------------------------------------------------

foreach ( glob( "Views/*.php" ) as $file ) {
    require_once $file;
}
